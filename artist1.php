                        <br>
                        <p>     The Philippine artist Fernando Amorsolo (1892-1972) was a portraitist and painter of rural land scapes. He is best known for his craftsmanship and mastery in the use of light. 
                        <br>
                        <br>    Fernando Amorsolo was born May 30, 1892, in the Paco district of Manila. At 13 he was apprenticed to the noted Philippine artist Fabian de la Rosa, his mother's first cousin. In 1909 Amorsolo enrolled at the Liceo de Manila and then attended the fine-arts school at the University of the Philippines, graduating in 1914. After working three years as a commercial artist and part-time instructor at the university, he studied at the Escuela de San Fernando in Madrid. For seven months he sketched at the museums and on the streets of Madrid, experimenting with the use of light and color. That winter he went to New York and discovered the works of the postwar impressionists and cubists, who became the major influence on his works. On his return to Manila, he set up his own studio.</p>
